
public class forloop {

	public static void main(String[] args) {

		int a[] = { 2, 3, 4, 5 };
		String b[] = { "S", "R", "A", "V", "A", "N" };

		for (int i = 1; i < a.length + 1; i++) {

			System.out.println(a[a.length - i]);

		}

		for (int i = 1; i < b.length + 1; i++) {

			System.out.println(b[b.length - i]);

		}

	}

}
